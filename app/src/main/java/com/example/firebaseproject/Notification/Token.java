package com.example.firebaseproject.Notification;

/**
 * Represents a client token
 */
public class Token {

    private String token;

    public Token() {

    }

    public Token(String token) {
        this.token = token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }
}
